class Solution {
  public:
    int kthMissing(vector<int> &arr, int k) {
        // Your code goes here
        
        unordered_set<int>st;
        
        for(int i:arr){
            st.insert(i);
        }
        
        
        int high = *max_element(arr.begin() , arr.end());
        
        for(int i=1;i<=high;i++){
            if(st.find(i)==st.end()){
                k--;
                if(k==0){
                    return i;
                }
            }
        }
        
        return high+k;
    }
};

