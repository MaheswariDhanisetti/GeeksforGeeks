//User function Template for C++
class Solution
{
public:
    int trailingZeroes(int number)
    {
        // Write Your Code here
       int count = 0;
       for (int i = 5; number / i >= 1; i *= 5){
          int temp = number / i;
          count = count + temp;
       }
       return count;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans  = ob.trailingZeroes(N);
        cout<<ans<<endl;
    }
    return 0;
}
